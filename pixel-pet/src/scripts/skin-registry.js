function renderSkin(skinId) {
  const skin = SKIN_REGISTRY[skinId]
  if (!skin) return ''
  return skin.grid.map(row => {
    const cells = row.split(' ').map(c =>
      c === '_' ? '<div class="px"></div>' : '<div class="px ' + c + '"></div>'
    ).join('')
    return '<div class="row">' + cells + '</div>'
  }).join('')
}

function applySkinColors(skinId) {
  const skin = SKIN_REGISTRY[skinId]
  if (!skin) return
  let styleEl = document.getElementById('skin-colors')
  if (!styleEl) {
    styleEl = document.createElement('style')
    styleEl.id = 'skin-colors'
    document.head.appendChild(styleEl)
  }
  const lines = Object.entries(skin.colors).map(([key, val]) =>
    '.pixel-art .px.' + key + ' { background: ' + val.bg + '; border: 1px solid ' + val.border + '; }'
  )
  styleEl.textContent = lines.join('\n')
}

function switchSkin(skinId) {
  const skin = SKIN_REGISTRY[skinId]
  if (!skin) return
  const petEl = document.getElementById('pet')
  const pixelArt = petEl ? petEl.querySelector('.pixel-art') : null
  if (!petEl || !pixelArt) return
  petEl.className = petEl.className.replace(/skin-\S+/g, '')
  petEl.classList.add('skin-' + skinId)
  petEl.querySelectorAll('.cat-tail, .dog-tail').forEach(el => el.remove())
  pixelArt.innerHTML = renderSkin(skinId)
  applySkinColors(skinId)
  if (skinId === 'cat') {
    const tail = document.createElement('div')
    tail.className = 'cat-tail'
    petEl.appendChild(tail)
  }
}
